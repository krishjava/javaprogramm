// Encapsulation 1
// Object in Demo.java
public class Login {
    private String name,pass;

    public void setName(String name){
        this.name=name;
    }
    public String getName(){
        return name;
    }
    public void setPass(String pass){
        this.pass=pass;
    }
    public String getPass(){
        return pass;
    }
    
}